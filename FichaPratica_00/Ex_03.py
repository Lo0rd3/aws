# pedir ao user altura e comprimento
altura = float(input("digite a altura: "))
largura = float(input("digite a largura : "))

#guarda o resultado das variaveis | calcula a area e perimetro
perimetro = altura * largura
area = (altura * 2) + (largura * 2)

#print do resultado 
print ("O perimetro do retangulo é:", perimetro)
print ("A área do retangulo é:" , area)